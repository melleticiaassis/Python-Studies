def dobro(numero):
    """
    Calcula o dobro do número fornecido.
    
    Parâmetros:
    numero (int ou float): valor que será dobrado

    Retorna:
    int ou float: o valor do número multiplicado por 2
    """
    # Aqui multiplicamos o número por 2
    resultado = numero * 2  # <-- Notação pessoal: multiplicação básica
    return resultado
